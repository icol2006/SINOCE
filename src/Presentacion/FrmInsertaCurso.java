    private void btnGuardarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCursoActionPerformed
        if (txtCodCurso.getText().isEmpty() || txtNomCurso.getText().isEmpty() || txtHorasCurso.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Para continuar, complete todos los campos solicitados");
        } else {
            int horas = 0, cod = 0;
            Curso cu = new Curso();
            try {
                cod = Integer.parseInt(txtCodCurso.getText());
            } catch(NumberFormatException nfe){
                JOptionPane.showMessageDialog(this, "El codigo de curso debe ser numerico", "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            try{
                horas = Integer.parseInt(txtHorasCurso.getText());
            } catch(NumberFormatException nfe){
                JOptionPane.showMessageDialog(this, "La cantidad de horas del curso debe ser numerico", "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            int resultado = cu.Insertar(cod, txtNomCurso.getText(), horas);
            
            if (resultado == Constantes.EXITO){
                JOptionPane.showMessageDialog(null, "¡Curso insertado de manera exitosa!", 
                    "Resultado de guardado", JOptionPane.INFORMATION_MESSAGE);
            }else{
                if (resultado == Constantes.ERROR_GUARDAR_COD_EXISTENTE){
                    JOptionPane.showMessageDialog(null, "¡El código de este curso existe!", 
                        "Resultado de guardado", JOptionPane.ERROR_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this, "Error al guardar curso", "Error",
                        JOptionPane.ERROR_MESSAGE);
                }
            }
            
        }

    }//GEN-LAST:event_btnGuardarCursoActionPerformed

