    private void btnBuscarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCursoActionPerformed
        if (txtCodCurso.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Para continuar, ingrese un c贸digo a buscar");
        } else {
            Curso cu = new Curso();
            int cod = 0;
            
            try {
                cod = Integer.parseInt(txtCodCurso.getText());
            } catch(NumberFormatException nfe){
                JOptionPane.showMessageDialog(this, "El codigo de curso debe ser numerico", "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            int respuesta = cu.consultarCursoPorCodigo(cod);
            
            if (respuesta != Constantes.EXITO){
                JOptionPane.showMessageDialog(this, "Curso no existe", "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }else{
                this.txtNomCurso.setText(cu.getNombreCurso());
                this.txtHorasCurso.setText(cu.getHoras()+"");
                
                JOptionPane.showMessageDialog(this, "Consulta exitosa", "Resultado",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnBuscarCursoActionPerformed

