    private void btnEliminarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCursoActionPerformed
        if (txtCodCurso.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Para continuar, ingrese un codigo a eliminar");
        } else {
            Curso cu = new Curso();
            int cod = 0;

            try {
                cod = Integer.parseInt(txtCodCurso.getText());
            } catch (NumberFormatException nfe) {
                JOptionPane.showMessageDialog(this, "El codigo de curso debe ser numerico", "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            int respuesta = cu.consultarCursoPorCodigo(cod);

            if (respuesta != Constantes.EXITO) {
                JOptionPane.showMessageDialog(this, "Curso no existe", "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                int resp = JOptionPane.showConfirmDialog(this, "Esta seguro de eliminar el curso "
                        + cu.getNombreCurso() + "?", "Confirmaci贸n",
                        JOptionPane.YES_NO_OPTION);

                if (resp == JOptionPane.YES_OPTION) {
                    resp = cu.eliminarCurso(cod);

                    if (respuesta == Constantes.EXITO) {
                        JOptionPane.showMessageDialog(this, "Elimincacion de curso exitosa", "Error",
                                JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        JOptionPane.showMessageDialog(this, "Eliminacion de curso fallida", "Error",
                        JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        }

    }//GEN-LAST:event_btnEliminarCursoActionPerformed

